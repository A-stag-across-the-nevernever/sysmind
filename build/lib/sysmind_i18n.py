def t(code: str, key: str) -> str:
    """Translated string, falling back to English then to the key itself."""
    return (WIZARD.get(code, {}).get(key)
            or WIZARD["english"].get(key)
            or key)


def coverage() -> Dict[str, int]:
    """How many keys each language actually defines. For spotting gaps."""
    return {code: sum(1 for k in KEYS if k in WIZARD.get(code, {}))
            for code, _, _ in LANGUAGES}
